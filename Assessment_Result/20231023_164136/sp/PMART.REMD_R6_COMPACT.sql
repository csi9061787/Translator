REPLACE PROCEDURE PMART.REMD_R6_COMPACT()
SQL SECURITY INVOKER
SP:BEGIN
  DECLARE SQLSTR  VARCHAR(4000) DEFAULT '';
  DECLARE P_YYYYMM NUMBER;
  DECLARE STORE_CS CURSOR FOR STORE_SQL;    
  DECLARE CONTINUE HANDLER FOR SQLSTATE '52010'  
  DELETE FROM #VT_REMD_R6_COMPACT;   
  SET SQLSTR = 'CREATE MULTISET VOLATILE TABLE #VT_REMD_R6_COMPACT  
    (    
       MONTH_ID NUMBER
    )
   NO PRIMARY INDEX ON COMMIT PRESERVE ROWS;';
  EXECUTE IMMEDIATE SQLSTR;  
  SET SQLSTR ='SELECT DISTINCT ROUND(REMDDT/100) AS YYYYMM FROM ' +
              ' ( ' +
              ' SELECT * FROM PSTAGE.DS_REMD0300_R6_I1 ' +
              ' ) AA ' ;
  PREPARE STORE_SQL FROM SQLSTR;
  OPEN STORE_CS;  
  L1:
  WHILE (SQLCODE =0) 
  DO    
     L1_1:
        BEGIN 
         FETCH STORE_CS INTO P_YYYYMM  ;
         IF SQLSTATE <> '00000' THEN LEAVE L1; END IF; 	   
         INSERT INTO  #VT_REMD_R6_COMPACT  (MONTH_ID ) VALUES (P_YYYYMM);   
         CALL PMART.NEWFILLBLANK(P_YYYYMM);
         CALL PMART.FILLBLANK(P_YYYYMM);
         CALL PMART.COMPACTMONTH(P_YYYYMM);
         CALL PMART.REMD_EX_TRANSFER(P_YYYYMM);
     END L1_1;
  END WHILE L1;      
  CLOSE STORE_CS;
END SP;