REPLACE   PROCEDURE PMART.PS_ORG_ID
(
)
SP:BEGIN
	DECLARE SQLSTR  VARCHAR(32000);
	DECLARE SQLCREATE  VARCHAR(30000);
	DECLARE P_JOB_ID  VARCHAR(20000);
	SET P_JOB_ID = (SELECT *  FROM #VT_PS_JOB_ID);
 	CALL PMART.P_DROP_TABLE ('#VT_PS_ORG_ID');
SET SQLSTR = ' 
SELECT ORG_ID FROM  PDATA.PRDREL_SET WHERE  JOB_ID = '+P_JOB_ID+'
'
; 
   SET SQLCREATE = 
   'CREATE MULTISET VOLATILE TABLE  #VT_PS_ORG_ID AS( '
   +SQLSTR 
   +'	) WITH DATA PRIMARY INDEX(ORG_ID) ON COMMIT PRESERVE ROWS;'
   ; 
EXECUTE IMMEDIATE SQLCREATE;   
END SP;