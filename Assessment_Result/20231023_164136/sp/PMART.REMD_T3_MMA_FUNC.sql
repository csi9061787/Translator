REPLACE PROCEDURE PMART.REMD_T3_MMA_FUNC(P_DAY_ID NUMBER,P_ORG_ID NUMBER)
SQL SECURITY INVOKER
SP:BEGIN
DECLARE SQLSTR  VARCHAR(4000) DEFAULT ''; 
  CALL PMART.P_DROP_TABLE ('#VT_REMD_T3_MMA_FUNC'); 
      SET SQLSTR = 
          'CREATE MULTISET VOLATILE TABLE #VT_REMD_T3_MMA_FUNC AS('+            
               'SELECT '+
               'A.STYPE_ID AS STYPE_ID, '+
               'A.TOT_AMT  AS TOT_AMT_LAST_YEAR, '+
               'A.TOT_CUST_NUM AS TOT_CUST_NUM_LAST_YEAR, '+
               'A.TOT_TKSL AS TOT_TKSL_LAST_YEAR '+
               'FROM PMART.REMD_FACT_STYPE_SUM A, '+
               '(SELECT '+
               'DISTINCT MMA_ID FROM PMART.STORE_TYPE_DIM) B '+
               'WHERE A.STYPE_ID=B.MMA_ID AND '+
               'A.ORG_ID='+P_ORG_ID+' AND '+
               'A.L_DAY_ID=ROUND('+P_DAY_ID+'/100)*100+1-10000 '+
           ' ) WITH DATA NO PRIMARY INDEX ON COMMIT PRESERVE ROWS;';
    EXECUTE IMMEDIATE SQLSTR; 
END SP;