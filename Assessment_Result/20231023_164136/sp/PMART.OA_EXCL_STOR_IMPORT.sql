REPLACE PROCEDURE PMART.OA_EXCL_STOR_IMPORT
(
	IN P_TIME_ID VARCHAR(6),
	IN P_EMP_NO VARCHAR(10),
	OUT DATA_CNT INTEGER
)
SQL SECURITY INVOKER
SP:BEGIN
	DECLARE  DATA_TIMESTAMP TIMESTAMP(0);
	SET DATA_TIMESTAMP = CURRENT_TIMESTAMP(0);
	DELETE PDATA.OA_EXCL_STOR WHERE TIME_ID = P_TIME_ID;
	INSERT INTO PDATA.OA_EXCL_STOR(TIME_ID, EXCL_ID, STORE_ID, EMP_NO, UPDATE_TIME)
	SELECT TIME_ID, EXCL_ID, CAST(STORE_NO AS INTEGER), P_EMP_NO, DATA_TIMESTAMP 
	FROM PSTAGE.OA_EXCL_STOR_IF;
	SET DATA_CNT = (SELECT COUNT(*) FROM PDATA.OA_EXCL_STOR WHERE UPDATE_TIME = DATA_TIMESTAMP );
END SP;