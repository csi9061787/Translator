REPLACE PROCEDURE PMART.FUNC_FACT_CARD_POINT_MEMBER
(
   IN P_GROUP VARCHAR(500),
   IN P_TIMEID VARCHAR(2000),
   IN P_ORGID VARCHAR(2000),
   IN P_CARDTYPE VARCHAR(500),
   IN P_FG_EX_TYPE VARCHAR(10),
   IN P_MEMBER_FG VARCHAR(10)
)
SP:BEGIN
   DECLARE SQLSTR  VARCHAR(2000);    
   DECLARE FGEXTYPE VARCHAR(10);
IF (TRIM(P_FG_EX_TYPE))='' THEN
    SET FGEXTYPE = '-1';
ELSE
    SET FGEXTYPE = P_FG_EX_TYPE;
END IF;
CALL PMART.P_DROP_TABLE ('#VT_FACT_CARD_POINT_MEMBER');
SET SQLSTR = ' CREATE MULTISET VOLATILE TABLE #VT_FACT_CARD_POINT_MEMBER AS('
                          +' SELECT '
                          + CASE WHEN P_GROUP LIKE '%RESPON_ID%' THEN Replace(P_GROUP,'RESPON_ID','RESPON_ID AS ORG_ID') ELSE P_GROUP END +',SUM(EX_AMT) EX_AMT,SUM(EX_POINT) EX_POINT,SUM(EX_CNT) EX_CNT'
                          + ' FROM PMART.FACT_CARD_POINT_MEMBER'
                          + CASE WHEN P_GROUP LIKE '%RESPON_ID%'  THEN  ' JOIN PMART.ORG_STORE ON ORG_ID=OSTORE_ID ' ELSE '' END
                          + ' WHERE ' 
                          + CASE WHEN TRIM(P_ORGID)='' AND POSITION('ORG_ID' IN P_GROUP)=0 THEN 'ORG_ID=-1 ' WHEN TRIM(P_ORGID)='' THEN '1=1'  ELSE 'ORG_ID IN('+P_ORGID+') ' END
                          + ' AND TIME_ID IN ('+P_TIMEID+')' 
                          + CASE WHEN TRIM(P_CARDTYPE)='' AND POSITION('CARD_TYPE' IN P_GROUP)=0 THEN ' AND CARD_TYPE=-1 ' WHEN TRIM(P_CARDTYPE)='' THEN ''  ELSE ' AND CARD_TYPE IN('+P_CARDTYPE+') ' END
                          + ' AND FG_EX_TYPE IN('+FGEXTYPE+') ';
       IF (TRIM(P_MEMBER_FG))<>'' THEN
             SET SQLSTR = SQLSTR + ' AND MEMBER_FG IN('+P_MEMBER_FG+') ';
       END IF;
       SET SQLSTR = SQLSTR + ' GROUP BY '+  P_GROUP                     
                                        + ' ) WITH DATA PRIMARY CHARINDEX('RESPON_ID','+CASE WHEN P_GROUP LIKE '%RESPON_ID%' THEN Replace(P_GROUP,'ORG_ID') ELSE P_GROUP END+') ON COMMIT PRESERVE ROWS;';
        EXECUTE IMMEDIATE SQLSTR;   
END SP;