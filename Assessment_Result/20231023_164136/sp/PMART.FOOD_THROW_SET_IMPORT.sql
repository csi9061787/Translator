REPLACE PROCEDURE PMART.FOOD_THROW_SET_IMPORT
(
	IN P_START_DATE INTEGER,
	IN P_EMP_NO VARCHAR(10),
	OUT DATA_CNT INTEGER
)
SQL SECURITY INVOKER
SP:BEGIN
	DECLARE DATA_TIMESTAMP TIMESTAMP(0);
	SET DATA_TIMESTAMP = CURRENT_TIMESTAMP(0);
	DELETE FROM PDATA.FOOD_THROW_SET 
	WHERE START_DATE >= TO_NUMBER(Replace(TO_CHAR(CURRENT_DATE+2, 'YYYY/MM/DD'), '/', ''));
	INSERT INTO PDATA.FOOD_THROW_SET
	SELECT P_START_DATE
		,TYPE_NAME
		,KND_NO
		,P_EMP_NO
		,DATA_TIMESTAMP
	FROM PDATA.FOOD_THROW_SET_IF;
	SET DATA_CNT = (SELECT COUNT(*) FROM PDATA.FOOD_THROW_SET WHERE UPDATE_TIME = DATA_TIMESTAMP );
END SP;