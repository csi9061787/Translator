REPLACE PROCEDURE PMART.REMD_S3_OSTORE_FUNC(P_DAY_ID NUMBER,P_RESPON_ID NUMBER)
SQL SECURITY INVOKER
SP:BEGIN
DECLARE SQLSTR  VARCHAR(4000) DEFAULT ''; 
  CALL PMART.P_DROP_TABLE ('#VT_REMD_S3_OSTORE_FUNC'); 
  LOCKING PMART.REMD_FACT FOR ACCESS;
  LOCKING PMART.LAST_ORG_DIM FOR ACCESS;  
      SET SQLSTR = 
          'CREATE MULTISET VOLATILE TABLE #VT_REMD_S3_OSTORE_FUNC AS('+            
               'SELECT '+
               'B.OSTORE_ID AS ORG_ID, '+
               'A.ACCU_CUST_NUM AS ACCU_CUST_NUM_LAST_YEAR, '+
               'A.ACCU_PLAN_STNUM AS ACCU_PLAN_STNUM_LAST_YEAR, '+
               'A.ACCU_SCUST_NUM AS ACCU_SCUST_NUM_LAST_YEAR '+
               'FROM PMART.REMD_FACT A INNER JOIN '+
               '(SELECT OSTORE_ID FROM PMART.LAST_ORG_DIM '+
               'WHERE RESPON_ID='+P_RESPON_ID+') B '+
               ' ON A.OSTORE_ID=B.OSTORE_ID '+
               ' INNER JOIN '+
               '(SELECT DISTINCT L_MONTH_END_DAY FROM PMART.YMWD_TIME '+               
               'WHERE L_MONTH_ID=TO_CHAR(ADD_MONTHS(CAST('+P_DAY_ID+'-19000000 AS DATE),  -12),''YYYYMM'') ) C '+
               ' ON A.L_DAY_ID=C.L_MONTH_END_DAY '+
           ' ) WITH DATA ON COMMIT PRESERVE ROWS;';
    EXECUTE IMMEDIATE SQLSTR; 
END SP;