REPLACE PROCEDURE PMART.REMD_S3_BRANCH_FUNC_PAUL(P_DAY_ID NUMBER,P_DEPT_ID NUMBER)
SQL SECURITY INVOKER
SP:BEGIN
DECLARE SQLSTR  VARCHAR(4000) DEFAULT ''; 
  CALL PMART.P_DROP_TABLE ('#VT_REMD_S3_BRANCH_FUNC_PAUL'); 
  LOCKING PMART.REMD_FACT_SUM FOR ACCESS;
  LOCKING PMART.LAST_ORG_DIM FOR ACCESS;
      SET SQLSTR = 
          'CREATE MULTISET VOLATILE TABLE #VT_REMD_S3_BRANCH_FUNC_PAUL AS('+            
      			   'SELECT '+
               'B.BRANCH_ID AS ORG_ID, '+
               'A.TOT_AMT  AS TOT_AMT_LAST_YEAR, '+
               'A.TOT_CUST_NUM AS TOT_CUST_NUM_LAST_YEAR, '+
               'A.TOT_TKSL AS TOT_TKSL_LAST_YEAR '+
               'FROM PMART.REMD_FACT_SUM A, '+
               '(SELECT DISTINCT BRANCH_ID FROM PMART.LAST_ORG_DIM WHERE DEPT_ID='+P_DEPT_ID+') B '+
               'WHERE A.ORG_ID=B.BRANCH_ID AND '+
               'A.L_DAY_ID=ROUND('+P_DAY_ID+'/100)*100+1-10000  '+
           ' ) WITH DATA NO PRIMARY INDEX ON COMMIT PRESERVE ROWS;';
    EXECUTE IMMEDIATE SQLSTR; 
END SP;