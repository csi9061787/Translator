REPLACE PROCEDURE PMART.REMD_SCAL_TOT_FUNC_PAUL(P_DAY_ID NUMBER)
SQL SECURITY INVOKER
SP:BEGIN
DECLARE SQLSTR  VARCHAR(4000) DEFAULT ''; 
  LOCKING PMART.REMD_FACT_SUM FOR ACCESS;
  LOCKING PMART.LAST_ORG_DIM FOR ACCESS;
  CALL PMART.P_DROP_TABLE ('#VT_REMD_SCAL_TOT_FUNC_PAUL'); 
  CALL PMART.REMD_ST_TOT_FUNC_PAUL(P_DAY_ID);
      SET SQLSTR = 
          'CREATE MULTISET VOLATILE TABLE #VT_REMD_SCAL_TOT_FUNC_PAUL AS('+            
                'SELECT '+
                'TOT_ID, '+
                'ORG_ID, '+
                'MAST_STORE_NUM AS R1, '+
                'AMT/DECODE(UPLOAD_STNUM,0,NULL,UPLOAD_STNUM) AS R2, '+
                'CUST_NUM/DECODE(UPLOAD_STNUM,0,NULL,UPLOAD_STNUM) AS R3, '+
                'AMT/DECODE(CUST_NUM,0,NULL,CUST_NUM) AS R4, '+
                '(ACCU_AMT/DECODE(ACCU_PLAN_STNUM,0,NULL,ACCU_PLAN_STNUM)) AS R5, '+
		' ( '+
		'SELECT ((SUM(ACCU_AMT) / SUM((DECODE(ACCU_PLAN_STNUM,0,NULL,ACCU_PLAN_STNUM) * DECODE(BUDGET_DEPT_AVG_AMT,0,NULL,BUDGET_DEPT_AVG_AMT)))) * 0.9561 * 100) AS AMT '+		
	        ' FROM ( '+
	        ' SELECT '+
	        '  CAST(A.ACCU_AMT AS NUMBER) AS ACCU_AMT '+
	        ' ,CAST(A.ACCU_PLAN_STNUM AS NUMBER) AS ACCU_PLAN_STNUM '+
	        ' ,CAST(A.BUDGET_DEPT_AVG_AMT AS NUMBER) AS BUDGET_DEPT_AVG_AMT '+
	        ' FROM PMART.REMD_FACT_SUM A '+
                ' WHERE L_DAY_ID = '+P_DAY_ID+' '+
                ' AND ORG_ID IN (SELECT DISTINCT DEPT_ID FROM PMART.LAST_ORG_DIM) '+
                '      ) B '+
	        ' ) AS R6,  '+
                ' (EX_ACCU_AMT/DECODE(EX_ACCU_PLAN_STNUM,0,NULL,EX_ACCU_PLAN_STNUM))/ '+
                ' (EX_TOT_AMT_LAST_YEAR/ '+
                '   DECODE(EX_TOT_PLAN_STNUM_LAST_YEAR,0,NULL,EX_TOT_PLAN_STNUM_LAST_YEAR))*100 '+
                'AS R7, '+
                ' (ACCU_AMT/DECODE(ACCU_PLAN_STNUM,0,NULL,ACCU_PLAN_STNUM))/ '+
                '   DECODE(TOT_AMT_LAST_MONTH/ '+
                '   DECODE(TOT_PLAN_STNUM_LAST_MONTH,0,NULL,TOT_PLAN_STNUM_LAST_MONTH), '+
                '   0,NULL,TOT_AMT_LAST_MONTH/ '+
                '   DECODE(TOT_PLAN_STNUM_LAST_MONTH,0,NULL,TOT_PLAN_STNUM_LAST_MONTH))*100 '+
                'AS R8, '+
				        '(ACCU_CUST_NUM/DECODE(ACCU_PLAN_STNUM,0,NULL,ACCU_PLAN_STNUM)) AS R9, '+
                '(EX_ACCU_CUST_NUM/DECODE(EX_ACCU_PLAN_STNUM,0,NULL,EX_ACCU_PLAN_STNUM))/ '+
                'DECODE( '+
                'EX_TOT_CUST_NUM_LAST_YEAR/ '+
                'DECODE(EX_TOT_PLAN_STNUM_LAST_YEAR,0,NULL,EX_TOT_PLAN_STNUM_LAST_YEAR) '+
                ',0,NULL, '+
                'EX_TOT_CUST_NUM_LAST_YEAR/ '+
                'DECODE(EX_TOT_PLAN_STNUM_LAST_YEAR,0,NULL,EX_TOT_PLAN_STNUM_LAST_YEAR) '+
                ')*100 AS R10, '+
				        '(ACCU_AMT/DECODE(ACCU_CUST_NUM,0,NULL,ACCU_CUST_NUM)) AS R11, '+
				        '(ACCU_AMT/DECODE(ACCU_CUST_NUM,0,NULL,ACCU_CUST_NUM))- '+
                '(EX_TOT_AMT_LAST_YEAR/ '+
                'DECODE(EX_TOT_CUST_NUM_LAST_YEAR,0,NULL,EX_TOT_CUST_NUM_LAST_YEAR)) '+
                'AS R12, '+
                '(ACCU_AMT*0.9561/DECODE(BUDGET_ST_TOT_AMT,0,NULL,BUDGET_ST_TOT_AMT))*100 AS R13 '+
                'FROM #VT_REMD_ST_TOT_FUNC_PAUL T1 '+
          ') WITH DATA NO PRIMARY INDEX ON COMMIT PRESERVE ROWS;';
    EXECUTE IMMEDIATE SQLSTR; 
END SP;