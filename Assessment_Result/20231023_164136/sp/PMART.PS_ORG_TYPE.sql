REPLACE   PROCEDURE PMART.PS_ORG_TYPE
(
)
SQL SECURITY INVOKER
SP:BEGIN
	DECLARE SQLSTR  VARCHAR(20000);
	DECLARE SQLCREATE  VARCHAR(30000);
	DECLARE P_JOB_ID  VARCHAR(20000);
	SET P_JOB_ID = (SELECT *  FROM SYSDBA.#VT_PS_JOB_ID);
 	CALL PMART.P_DROP_TABLE ('#VT_PS_ORG_TYPE');
SET SQLSTR = ' 
SELECT ORG_TYPE FROM  PDATA.PRDREL_SET WHERE  JOB_ID = '+P_JOB_ID+'
'
; 
   SET SQLCREATE = 
   'CREATE MULTISET VOLATILE TABLE  #VT_PS_ORG_TYPE AS( '
   +SQLSTR 
   +'	) WITH DATA PRIMARY INDEX(ORG_TYPE) ON COMMIT PRESERVE ROWS;'
   ;
EXECUTE IMMEDIATE SQLCREATE;   
END SP;