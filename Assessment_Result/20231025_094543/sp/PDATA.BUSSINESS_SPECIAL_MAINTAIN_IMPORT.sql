CREATE PROCEDURE  PDATA.BUSSINESS_SPECIAL_MAINTAIN_IMPORT
(
OUT DATA_CNT INTEGER
)
SQL SECURITY INVOKER
SP:BEGIN
DECLARE  DATA_TIMESTAMP INTEGER;
SET DATA_TIMESTAMP =CAST(TO_CHAR(CURRENT_DATE, 'YYYYMMDD') AS INT);
DELETE PDATA.BUSSINESS_SPECIAL_MAINTAIN  ALL;
INSERT INTO PDATA.BUSSINESS_SPECIAL_MAINTAIN(OSTORE_NO,OSTORE_NO_TYPE,UPDATE_TIME)
SELECT OSTORE_NO,OSTORE_NO_TYPE,DATA_TIMESTAMP FROM PSTAGE.BUSSINESS_SPECIAL_MAINTAIN_IF;
SET DATA_CNT = (SELECT COUNT(*) FROM PDATA.BUSSINESS_SPECIAL_MAINTAIN WHERE UPDATE_TIME =DATA_TIMESTAMP );
END SP;