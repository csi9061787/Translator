REPLACE PROCEDURE PMART.REMD_S2_OSTORE_FUNC_PAUL
(P_DAY_ID NUMBER,P_RESPON_ID NUMBER)
SQL SECURITY INVOKER
SP:BEGIN
DECLARE SQLSTR  VARCHAR(4000) DEFAULT ''; 
  CALL PMART.P_DROP_TABLE ('#VT_REMD_S2_OSTORE_FUNC_PAUL'); 
  LOCKING PMART.REMD_FACT FOR ACCESS;
  LOCKING PMART.LAST_ORG_DIM FOR ACCESS;
  LOCKING PMART.YMWD_TIME FOR ACCESS;
      SET SQLSTR = 
          'CREATE MULTISET VOLATILE TABLE #VT_REMD_S2_OSTORE_FUNC_PAUL AS('+            
			  'SELECT '+
                'B.OSTORE_ID AS ORG_ID, '+
                'A.TOT_AMT AS TOT_AMT_LAST_MONTH, '+
                'A.TOT_PLAN_STNUM AS TOT_PLAN_STNUM_LAST_MONTH, '+
                'A.TOT_TKSL AS TOT_TKSL_LAST_MONTH '+
                'FROM PMART.REMD_FACT A, '+
                '(SELECT OSTORE_ID FROM PMART.LAST_ORG_DIM '+
                'WHERE RESPON_ID='+P_RESPON_ID+') B, '+
                'PMART.YMWD_TIME C '+
                'WHERE '+
                'A.OSTORE_ID=B.OSTORE_ID AND '+
                'C.L_DAY_ID='+P_DAY_ID+' AND '+
                'A.L_DAY_ID=C.L_DAY_LAST_DAY_LAST_MONTH '+
           ' ) WITH DATA NO PRIMARY INDEX ON COMMIT PRESERVE ROWS;';
    EXECUTE IMMEDIATE SQLSTR; 
END SP;