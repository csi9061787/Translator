REPLACE PROCEDURE  PDATA.IMP_GP_PRD_BARCODE()
SP:BEGIN
DELETE PDATA.NECARBO_CMDT WHERE  PRJ_TYPE = 'GP';
INSERT	INTO PDATA.NECARBO_CMDT
(FM_CODE, BARCODE,FM_NAME, START_DATE, END_DATE, CLABLE_AMT, USER_ID, UPDATE_TIME,
		PRJ_TYPE, PRJ_NAME)
SELECT C.FM_CODE,D.BARCODE,C.FM_NAME,20200701 ,20200701 ,0.00 ,'ADMIN' ,CURRENT_TIMESTAMP(0) ,'GP','環保集點'
FROM PDATA.PBMCMDT  C 
INNER JOIN  
(SELECT A.FM_CODE,B.BARCODE FROM PDATA.PBMCMDT_BR A
INNER JOIN PTEMP.TEMP_GREENPOINT_PRD_BARCODE B ON A.BARCODE  = B.BARCODE) D ON D.FM_CODE = C.FM_CODE ;
END SP;