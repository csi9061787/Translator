REPLACE PROCEDURE PMART.REMD_T2_GRP_FUNC
(P_L_DAY_ID NUMBER,P_ORG_ID NUMBER)
SQL SECURITY INVOKER
SP:BEGIN
DECLARE SQLSTR  VARCHAR(4000) DEFAULT ''; 
  CALL PMART.P_DROP_TABLE ('#VT_REMD_T2_GRP_FUNC'); 
      SET SQLSTR = 
          'CREATE MULTISET VOLATILE TABLE #VT_REMD_T2_GRP_FUNC AS('+            
               'SELECT '+
               'A.GRP_ID AS STYPE_ID, '+
               'A.TOT_AMT AS TOT_AMT_LAST_MONTH, '+
               'A.TOT_PLAN_STNUM AS TOT_PLAN_STNUM_LAST_MONTH, '+
               'A.TOT_TKSL AS TOT_TKSL_LAST_MONTH '+
               'FROM PMART.REMD_FACT_GRP_SUM A, '+
               '(SELECT GRP_ID FROM PMART.REMD_GRP_M) B, '+
               ' PMART.YMWD_TIME C '+
               'WHERE A.GRP_ID=B.GRP_ID AND '+
               'A.ORG_ID='+P_ORG_ID+' AND '+
               'C.L_DAY_ID='+P_L_DAY_ID+' AND '+
               'A.L_DAY_ID=C.L_DAY_LAST_DAY_LAST_MONTH '+
           ' ) WITH DATA NO PRIMARY INDEX ON COMMIT PRESERVE ROWS;';
    EXECUTE IMMEDIATE SQLSTR; 
END SP;