REPLACE PROCEDURE PMART.WDWH_5_1_GATDATE_FUNC()
SQL SECURITY INVOKER
SP:BEGIN
    CALL PMART.P_DROP_TABLE ('#VT_WDWH_5_1_GATDATE_FUNC');
	 CREATE MULTISET VOLATILE TABLE #VT_WDWH_5_1_GATDATE_FUNC AS
	(
			SELECT MAX(PUBLISH_DATE_DATE) AS PUBLISH_DATE 
			FROM  PMART.PCB_V_PRDANTICIPATE 
			WHERE PUBLISH_DATE_DATE< CAST(CURRENT_DATE AS FORMAT 'YYYY-MM-DD')
	) WITH DATA NO PRIMARY INDEX ON COMMIT PRESERVE ROWS; 
END SP;